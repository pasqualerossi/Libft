#include "libft.h"

void	*memmove(void *dest, const void *src, size_t len)
{
	char	*tmp;

	tmp = (char *)malloc(sizeof(char) * len);
	if (tmp == NULL)
		return (NULL);
	ft_memcpy(tmp, src, len);
	ft_memcpy(dest, tmp, len);
	free(tmp);
	return (dest);
}
